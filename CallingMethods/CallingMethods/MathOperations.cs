using System;


namespace CallingMethods
{
    public class MathOperations
    {
        // multiply the num variable by 2
        public int MultiplyByTwo(int num)
        {
            return num * 2;
        }

        // add ten to the num variable
        public int AddTen(int num)
        {
            return num + 10;
        }

        // subtract five from the numb variable
        public int SubtractFive(int num)
        {
            return num - 5;
        }
    }
}
